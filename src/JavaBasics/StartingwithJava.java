package JavaBasics;

public class StartingwithJava {

	public static void main(String[] args) {
		int a=10;
		int b=50;
		int c=a+b;
		
		
		System.out.println("Value of a is:" +a);
		System.out.println("Value of b is:" +b);
		System.out.println("Value of c is:" +c);

		
		float f=30.30f;
		float f1=60.30f;
		float e= f+f1;
		float g=f1-f;
		
		System.out.println("Value of e is:" +e);
		System.out.println("Value of g is:" +g);

	    double d=123.456;
	    double d1=23.456;
	    double h=d-d1;
	    
		System.out.println("Value of h is:" +h);

		int i=312;
		int j=20;
		float div=i/j;
		double div1=i/j;
	
		
		System.out.println("Value of float div is:" +div);
		System.out.println("Value of div1 is:" +div1);

	}

}
