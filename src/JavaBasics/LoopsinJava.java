package JavaBasics;

public class LoopsinJava {

	public static void main(String[] args) {
		int a;//declaration
		int b=20;//initialization
		//============================
		
		//for loop
		
		//initialization condition increment,c++ is increment
		for(int c=1;c<5;c++) {//c=1,1<5=true,c=2,2<5=true....1,2,3,4
		    System.out.println("Value of c is "+c);
		    //System.out.println("Core Java - For loop session");
		    
		}

		for(int d=5;d>1;d--) {//d-- is decrement
		System.out.println("Value of d is "+d);


	}
	}
}
